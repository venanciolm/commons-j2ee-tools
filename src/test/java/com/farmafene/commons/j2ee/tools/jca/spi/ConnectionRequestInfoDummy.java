package com.farmafene.commons.j2ee.tools.jca.spi;

import java.util.UUID;

import com.farmafene.commons.j2ee.tools.jca.spi.ConnectionRequestInfoSPI;

@SuppressWarnings("serial")
public class ConnectionRequestInfoDummy extends ConnectionRequestInfoSPI {

	private UUID uuid = UUID.randomUUID();

	public ConnectionRequestInfoDummy() {

	}

	/**
	 * {@inheritDoc}
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		StringBuilder sb = new StringBuilder();
		sb.append(getClass().getSimpleName()).append("=[");
		sb.append("UUID=").append(uuid);
		sb.append("]");
		return sb.toString();
	}

	/**
	 * {@inheritDoc}
	 * 
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((uuid == null) ? 0 : uuid.hashCode());
		return result;
	}

	/**
	 * {@inheritDoc}
	 * 
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ConnectionRequestInfoDummy other = (ConnectionRequestInfoDummy) obj;
		if (uuid == null) {
			if (other.uuid != null)
				return false;
		} else if (!uuid.equals(other.uuid))
			return false;
		return true;
	}
}
